import math


def main(y, x, z):
    n = len(y)
    y = [0] + y
    x = [0] + x
    z = [0] + z
    res = 0
    for i in range(1, n):
        res += 91 * math.tan(x[n+1-i] - y[math.ceil(i/3)] ** 2 / 21 - 30 * z[i] ** 3)
    return res


